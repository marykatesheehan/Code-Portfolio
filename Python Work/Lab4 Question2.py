#Lab 4 Question 2: Find How Many Miles Per Gallon a Car Uses

miles_driven = eval(input("How many miles did you drive in your car?"))
gallons_used = eval(input("How many gallons of gas did your car use?"))

mpg = miles_driven / gallons_used
print("You car goes", mpg, "miles per gallon.")
