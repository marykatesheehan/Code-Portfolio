# 1)ask the user for a given speed and put it in a variable
# 2)ask the user for a given amount of time and put it in a variable
# 3)calculate the distance they went (distance = speed * time)
# 4)print the distance back to the user

speed = eval(input("Enter how fast your went in your car here in miles per hour: "))
time = eval(input("Enter how many hours you drove in your car here: "))
distance = speed * time
print("You drove", distance, "miles.")
