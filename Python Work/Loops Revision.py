for x in [1,2,3,4,5]: #prints the list
    print(x)

for x in ["white","red","yellow"]: #prints each string in list
    print(x)

for x in range(10): #gives you the valies from 0 to 9 (default start is 0 and counts ten numbers)
    print(x)
    
for x in range(10):
    print("Programming is fun!") #prints the sentence 10 times

for x in range(1, 10): #prints number 1-9 (10-1)
    print(x)

for x in range(1, 10, 3): #counts by three from 1 to 10
    print(x)

