#Meters to Miles, Feet, and Inches

meters = eval(input("Insert number of meters: "))
miles = round(meters / 1609.34, 2)
feet = round(meters * 3.2804, 2)
inches = round(meters * 39.37, 2)
print(meters, "meters is equal to", miles, "miles.")
print(meters, "meters is equal to", feet, "feet.")
print(meters, "meters is equal to", inches, "inches.")
